// Package schema provides tools to dump table schema from GORM models.
package schema

import (
	"fmt"
	"os"
)

type Schema interface {
	CreateTableSQL(tableName string) (string, error)
	// IndexSQL returns CREATE INDEX (and similar) statements for the table. May be nil if indexes are inlined in CreateTableSQL (e.g. MySQL).
	IndexSQL(tableName string) ([]string, error)
	Tables() ([]string, error)
}

func DumpSchema(s Schema, outPath string) error {
	file, err := os.Create(outPath)
	if err != nil {
		return fmt.Errorf("create file: %w", err)
	}
	defer file.Close()
	_, _ = file.WriteString("-- DDL from GORM models\n-- Generated by schema tool\n-- DO NOT EDIT THIS FILE DIRECTLY\n\n")
	tables, err := s.Tables()
	if err != nil {
		return fmt.Errorf("get tables: %w", err)
	}
	for _, tableName := range tables {
		_, _ = file.WriteString("-- Table: " + tableName + "\n")
		createSQL, err := s.CreateTableSQL(tableName)
		if err != nil {
			return fmt.Errorf("create table sql: %w", err)
		}
		_, _ = file.WriteString(createSQL)
		indexSQLs, err := s.IndexSQL(tableName)
		if err != nil {
			return fmt.Errorf("index sql for table %s: %w", tableName, err)
		}
		for _, idx := range indexSQLs {
			_, _ = file.WriteString(idx)
		}
		_, _ = file.WriteString("\n")
	}
	return nil
}
